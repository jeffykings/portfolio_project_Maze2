/**
 * main - Entry point of the program
 *
 * Return: 0 on success, 1 on failure
 */
int main(void)
{
    GameState game;
    game.is_running = true;
    game.player_pos.x = 22;
    game.player_pos.y = 12;
    game.player_angle = 0;
    game.show_map = false;

    if (!init_sdl(&game))
    {
        return 1;
    }

    if (!load_textures())
    {
        cleanup(&game);
        return 1;
    }

    /** Main game loop */
    while (game.is_running)
    {
        handle_events(&game);
        update_game_state(&game);
        render_frame(&game);

        SDL_Delay(16);  /** Cap to ~60 FPS */
    }

    cleanup(&game);
    return 0;
}
